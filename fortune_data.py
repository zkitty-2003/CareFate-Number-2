def get_base_fortune(weekday: str, category: str) -> str:
    """
    Retrieve raw base daily fortune prediction from simulated external API database.
    """
    # Sanitize inputs
    clean_weekday = weekday.strip().replace("วัน", "")
    if clean_weekday not in FORTUNE_DATABASE:
        clean_weekday = "อาทิตย์" # Default fallback
        
    clean_category = category.strip().lower()
    if clean_category not in ["love", "finance", "work", "health", "overall"]:
        clean_category = "overall" # Default fallback
        
    return FORTUNE_DATABASE[clean_weekday][clean_category]
